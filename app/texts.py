"""Статичные тексты интерфейса и помощники форматирования (markdown MAX)."""
from __future__ import annotations

# Символ кристалла используем единообразно по всему боту.
GEM = "💎"

# Спецсимволы markdown MAX: ** _ ~ ` [ ] ( ). Экранируем в пользовательском тексте,
# чтобы имя вида «**Артём**» не ломало разметку.
_SPECIAL = "\\*_~`[]()"


def esc(text: str | None) -> str:
    """Экранирует markdown-спецсимволы в пользовательском тексте."""
    if not text:
        return ""
    out = []
    for ch in str(text):
        if ch in _SPECIAL:
            out.append("\\" + ch)
        else:
            out.append(ch)
    return "".join(out)


def gems(n: int) -> str:
    return f"{GEM} {n}"


